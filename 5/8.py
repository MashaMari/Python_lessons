weather = input('Какая сегодня погода? - Солнечная, Дождливая, Морозная, Шторм: ')
if weather == 'Солнечная':
    print('Наденьте сегодня футболку и джинсы')
elif weather == 'Дождливая':
    print('Наденьте сегодня куртку и возьмите зонт')
elif weather == 'Шторм':
    print('Не выходи никуда')     
else:
    print('Наденьте сегодня зимнюю куртку и ботинки')       
